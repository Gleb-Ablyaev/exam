#На языке Питон написать программу, которая из файла читает объекты, разделенные  пробелами, выводит их на экран, производит заданную обработку и  результат выводит на экран. Обработка: через регулярные выражения найти сумму и произведение натуральных восьмеричных чисел, начинающихся с трех 3.

import re

# Функция для чтения чисел из файла и нахождения суммы и произведения натуральных восьмеричных чисел,
# начинающихся с трех 3
def process_file(filename):
    # Переменные для хранения суммы и произведения
    sum_octal = 0
    prod_octal = 1

    # Чтение файла
    with open(filename, 'r') as file:
        # Чтение каждой строки
        for line in file:
            # Использование регулярного выражения для поиска всех чисел
            numbers = re.findall(r'\b3[0-7]+\b', line)

            # Проход по найденным числам
            for number in numbers:
                # Преобразование восьмеричного числа в десятичное и обновление суммы и произведения
                octal_decimal = int(number, 8)
                sum_octal += octal_decimal
                prod_octal *= octal_decimal

    # Вывод результата на экран
    print("Сумма натуральных восьмеричных чисел, начинающихся с трех 3:", sum_octal)
    print("Произведение натуральных восьмеричных чисел, начинающихся с трех 3:", prod_octal)

# Вызов функции с указанием имени файла
process_file("input.txt")  # Замените "input.txt" на имя вашего файла с данными
